public class Pipe
{
    private int pipeDiam;
    private int pipeLength;

    public Pipe(int pipeDiam, int pipeLength)
    {
        this.pipeDiam = pipeDiam;
        this.pipeLength = pipeLength;
    }

    public int getPipeDiam()
    {
        return pipeDiam;
    }

    public int getPipeLength()
    {
        return pipeLength;
    }
    
}
